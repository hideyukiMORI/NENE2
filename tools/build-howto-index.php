<?php

/**
 * Regenerates the data-driven howto indexes so they stay in sync with the
 * files on disk:
 *
 * - English (`docs/howto`): a "Browse by category" index built from each
 *   guide's YAML frontmatter (every guide, grouped into the seven categories),
 *   injected between the AUTO-INDEX markers in README.md, plus a `by-tag.md`
 *   page grouping guides by tag.
 * - Other locales: a flat, alphabetical "Full Index" built from each guide's
 *   `# H1` (translations carry no frontmatter), injected into README.md.
 *
 * The manual curation in each README (the "I want to…" finder table) is left
 * untouched; only the content between the AUTO-INDEX markers is regenerated.
 *
 * Usage: php tools/build-howto-index.php
 */

declare(strict_types=1);

require_once __DIR__ . '/../vendor/autoload.php';

use Symfony\Component\Yaml\Yaml;

$root = dirname(__DIR__);

const AUTO_INDEX_START = '<!-- AUTO-INDEX:START (generated by `composer howto:index` — do not edit by hand) -->';
const AUTO_INDEX_END   = '<!-- AUTO-INDEX:END -->';

/** Files in a howto directory that are indexes, not guides. */
const NON_GUIDE_FILES = ['README.md', 'by-tag.md'];

/**
 * Category slug → display label, in the order they should appear.
 *
 * @var array<string, string>
 */
const CATEGORY_LABELS = [
    'getting-started' => 'Getting Started',
    'auth'            => 'Authentication & Authorization',
    'security'        => 'Security',
    'database'        => 'Database',
    'api-design'      => 'API Design',
    'infrastructure'  => 'Background & Infrastructure',
    'product'         => 'Product Features',
];

/**
 * Locale howto directories (relative to the repository root) and the localized
 * strings used when (re)generating their flat index section.
 *
 * @var array<string, array{dir: string, h1: string, heading: string, note: string}>
 */
$locales = [
    'en' => [
        'dir'     => 'docs/howto',
        'h1'      => 'Howto Guides Index',
        'heading' => 'Browse by category',
        'note'    => '',
    ],
    'ja' => [
